def rt(text, bold=False, italic=False, code=False, color="default"):
    """Create a rich_text element."""
    t = {"type": "text", "text": {"content": text}}
    annotations = {}
    if bold:
        annotations["bold"] = True
    if italic:
        annotations["italic"] = True
    if code:
        annotations["code"] = True
    if color != "default":
        annotations["color"] = color
    if annotations:
        t["annotations"] = annotations
    return t


def heading_1(text):
    return {"type": "heading_1", "heading_1": {"rich_text": [rt(text)]}}


def heading_2(text):
    return {"type": "heading_2", "heading_2": {"rich_text": [rt(text)]}}


def heading_3(text):
    return {"type": "heading_3", "heading_3": {"rich_text": [rt(text)]}}


def paragraph(rich_text_list):
    if isinstance(rich_text_list, str):
        rich_text_list = [rt(rich_text_list)]
    return {"type": "paragraph", "paragraph": {"rich_text": rich_text_list}}


def paragraph_bold(text):
    return paragraph([rt(text, bold=True)])


def bullet(rich_text_list):
    if isinstance(rich_text_list, str):
        rich_text_list = [rt(rich_text_list)]
    return {"type": "bulleted_list_item", "bulleted_list_item": {"rich_text": rich_text_list}}


def callout(text, emoji="💡"):
    if isinstance(text, str):
        text = [rt(text)]
    return {
        "type": "callout",
        "callout": {
            "rich_text": text,
            "icon": {"type": "emoji", "emoji": emoji},
        },
    }


def quote_block(text):
    if isinstance(text, str):
        text = [rt(text)]
    return {"type": "quote", "quote": {"rich_text": text}}


def divider():
    return {"type": "divider", "divider": {}}


def table_block(rows, has_header=True):
    """Create a table block. rows = list of lists of strings."""
    width = len(rows[0]) if rows else 0
    table_rows = []
    for row in rows:
        cells = []
        for cell in row:
            cells.append([rt(str(cell))])
        table_rows.append({"type": "table_row", "table_row": {"cells": cells}})
    return {
        "type": "table",
        "table": {
            "table_width": width,
            "has_column_header": has_header,
            "has_row_header": False,
            "children": table_rows,
        },
    }


def send_blocks(blocks, label=""):
    """Send blocks in batches of 100."""
    batch_size = 100
    for i in range(0, len(blocks), batch_size):
        batch = blocks[i : i + batch_size]
        data = {"children": batch}
        resp = requests.patch(API_URL, headers=HEADERS, json=data)
        batch_label = f" [{label} batch {i // batch_size + 1}]" if label else ""
        if resp.status_code == 200:
            print(f"  ✅ Sent {len(batch)} blocks{batch_label}")
        else:
            print(f"  ❌ Error{batch_label}: {resp.status_code}")
            print(f"     {resp.text[:500]}")
            return False
        if i + batch_size < len(blocks):
            time.sleep(0.4)
    return True


